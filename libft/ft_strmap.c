#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	count;

	if (!s)
		return (0);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (0);
	count = 0;
	while (s[count])
	{
		str[count] = (char)f(s[count]);
		count++;
	}
	str[count] = '\0';
	return (str);
}

#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned int		num;
	int					sign;
	int					i;

	num = 0;
	sign = 1;
	i = 0;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if (num > 2147483648 && sign == -1)
		return (0);
	else if (num > 2147483648 && sign == 1)
		return (-1);
	return (num * sign);
}
